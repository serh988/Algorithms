// Функция, которая добавляет новые элементы в массив
function extendArray(arr, newElementCount) {
    // Создаем копию исходного массива
    let newArr = [...arr];

    // Добавляем заданное количество новых элементов в массив
    for (let i = 0; i < newElementCount; i++) {
        newArr.push(null);  // Можно заменить 'null' на любое значение, если требуется
    }

    return newArr;
}

// Пример использования
let originalArray = [1, 2, 3, 4, 5];
let newElementCount = 3; // Количество новых элементов
let extendedArray = extendArray(originalArray, newElementCount);

console.log("Исходный массив:", originalArray); // [1, 2, 3, 4, 5]
console.log("Новый массив:", extendedArray);    // [1, 2, 3, 4, 5, null, null, null]

// Пример использования амортизированного анализа: динамическое расширение массива
// Операция добавления элементов в массив может происходить за O(1), 
// но при расширении массива на новый размер (удвоение) это будет стоить O(n).
// Однако амортизированная стоимость вставки в динамический массив равна O(1).

