// Функция для проверки корректности скобочной последовательности
function isValidSequence(sequence) {
    const stack = [];
    const pairs = {
        ')': '(',
        '}': '{',
        ']': '['
    };

    for (let char of sequence) {
        // Если текущий символ - открывающая скобка, добавляем её в стек
        if (char === '(' || char === '{' || char === '[') {
            stack.push(char);
        } 
        // Если текущий символ - закрывающая скобка
        else if (char === ')' || char === '}' || char === ']') {
            // Проверяем, соответствует ли закрывающая скобка верхнему элементу стека
            if (stack.length === 0 || stack.pop() !== pairs[char]) {
                return false; // Если нет соответствия, последовательность неправильная
            }
        }
    }

    // Если стек пуст, последовательность правильная
    return stack.length === 0;
}

// Примеры использования
console.log(isValidSequence("([{}])")); // Вывод: true
console.log(isValidSequence("([)]"));   // Вывод: false
console.log(isValidSequence("{()]"));   // Вывод: false

